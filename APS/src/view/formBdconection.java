    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String host = txtHostname.getText();
        String banco = txtBanco.getText();
        String user = txtUser.getText();
        String password = txtSenha.getText();
        
        if(host.equals("") || banco.equals("") || user.equals("") || password.equals("")){
            JOptionPane.showMessageDialog(null,"Por favor preencha os campos");
        }
        else{
            try{
            Conexao conn = new Conexao(host, banco, user, password);
            conn.getConnection();
            JOptionPane.showMessageDialog(null,"Conectado com sucesso");
            formListar form = new formListar();
            form.setVisible(true);
            }catch (SQLException e){
               JOptionPane.showMessageDialog(null,"Erro de conexão:\t" + e.getMessage());
            }catch (ClassNotFoundException e){
               JOptionPane.showMessageDialog(null,"ClassNotFound:\t" + e.getMessage());
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

